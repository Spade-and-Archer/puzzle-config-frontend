export function PickTagGroupIcon(props){
    const [curQuery, setCurQuery] = useState("");
    const [curIcon, setCurIcon] = useState(props.defaultIcon || "mdiCancel")
    const [curTag, setCurTag] = useState("")
    const [results, setResults] = useState([])
    const [curColor, setCurColor] = useState(props.defaultColor || "#424242")
    const [doSearch, asdfklja] = useState(new BufferedAction((query)=>{
        if(query === ""){
            setResults([]);
            return;
        }
        if(query.includes("tag:")){
            let tag = query.replace("tag:", "");
            setResults(iconsByTag[tag]);
            console.log(`tag is ${tag}`)
            return;
        }
        let scoredIcons = {};
        let keywords = query.toLowerCase().replace(/[^a-zA-Z0-9 ]/g, "").split(" ");
        Object.keys(icons).forEach((iconName)=>{
            let lowerIconName = iconName.toLowerCase()
            let score = keywords.reduce((curScore, keyword)=>{
                if( lowerIconName.includes(keyword)){
                    return curScore + 1;
                }
                return curScore;
            }, 0);
            if(score > 0){
                scoredIcons[iconName] = score;
            }
        })
        let sortedIconNames = Object.keys(scoredIcons);

        sortedIconNames.sort((a, b)=>{
            return scoredIcons[a] - scoredIcons[b];
        })
        setResults(sortedIconNames.slice(0, 300));
    }, 400, true, false))
    const inputID = useId();

    let iconNameWithBreaks = curIcon.replace(/([A-Z])/g, "​$1")
    return <Dialog open={props.open} onClose={props.onClose} fullWidth={true}
                   maxWidth={"md"} className={"PickTagGroupIcons"}>

        <div className={"SearchBarWrapper"}>
            <DialogTitle>
                Select an Icon
            </DialogTitle>

            <FormControl sx={{ m: 1, minWidth: '25ch' }} variant="outlined" className={"SearchBar"}>
                <InputLabel htmlFor={inputID}>Search:</InputLabel>
                <OutlinedInput
                    htmlFor={inputID}
                    value={curQuery}
                    onChange={(e)=>{
                        setCurQuery(e.target.value)
                        doSearch.trigger(e.target.value);
                    }}
                    fullWidth
                    endAdornment={
                        <InputAdornment position="end">
                            <IconButton
                                aria-label="toggle password visibility"
                                onClick={()=>{} }
                                edge="end"

                            >
                                <SearchIcon/>
                            </IconButton>
                        </InputAdornment>
                    }
                    label="Search:"
                />
            </FormControl>
        </div>
        <div className={"IconSearchResultsPanel"}>
            {results.map((iconKey)=>{
                return <IconButton onClick={(e)=>{setCurIcon(iconKey); if(props.onChange){props.onChange({value: iconKey})}}}>
                    <TagIcon  size={2} iconName={iconKey} />
                </IconButton>
            })}
            {(curQuery === "") && Object.keys(iconsByTag).map((tagKey)=>{
                return <div className={"IconCategory"}>
                    <IconButton onClick={(e)=>{setCurQuery(`tag:${tagKey}`); doSearch.trigger(`tag:${tagKey}`);}}>
                    <TagIcon  size={2} iconName={iconsByTag[tagKey][0]} />
                </IconButton>
                    <Typography variant={"caption"}>{tagKey}</Typography>
                </div>
            })}

        </div>
        <div className={"IconSearchPreviewPanel"}>
            <div className={"IconPreview"}>
                {/*<Typography variant={"h6"}>Name:</Typography>*/}


                <TagIcon size={4} color={curColor} iconName={curIcon}/>
                <div className={"IconPreviewText"}>

                    <Typography variant={"subtitle2"} className={"NameOfIconPreview"}>{iconNameWithBreaks}</Typography>
                    {/*this is the longest icon name*/}
                    <Typography variant={"subtitle2"} className={"dummyIconPreviewText"}>mdi​Microsoft​Xbox​Controller</Typography>
                    <Typography variant={"subtitle2"} className={"dummyIconPreviewText"}>​Battery​Charging​asdfsa​sfda</Typography>
                </div>
            </div>
            <div className={"colorSelector"}>
                {Object.keys(IconColorReference).map((colorName)=>{
                    return <div className={"colorColumn"}>
                        {["900", "700", "500", "300", "a200", "a700"].map((colorNum)=>{
                            let colorHexCode = IconColorReference[colorName][colorNum];
                            if(colorHexCode){
                                let entryClassName = " colorEntry "
                                if(curColor === colorHexCode){
                                    entryClassName += " colorEntry--selected"
                                }
                                return <div className={entryClassName } style={{backgroundColor: colorHexCode}} onClick={()=>{
                                    setCurColor(colorHexCode);
                                }
                                }/>
                            }
                        })}

                    </div>
                })}
            </div>
            <div className={"PreviewPanelActionSpacer"}/>
            <div className={"IconSearchFinished"}>
                <Button variant="outlined" color={"primary"}>Cancel</Button>
                <Button variant={"contained"} color={"primary"} onClick={()=>{
                    if(props.onClose){
                        props.onClose({
                            iconName: curIcon,
                            iconColor: curColor
                        }, true)
                    }

                }

                }>Save</Button>
            </div>

        </div>
    </Dialog>
}

